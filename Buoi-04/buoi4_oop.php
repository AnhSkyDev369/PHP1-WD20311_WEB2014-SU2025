//  OOP là viết tắt của lập trình hướng đối tượng
//  LẬP TRÌNH HƯỚNG ĐỐI TƯỢNG TRONG OOP
//  Lập trình hướng đối tượng là gì ?
//  -   Là một kỹ thuật lập trình (Được sử dụng trong hầu hết các ngôn ngữ)
//  -   Kỹ thuật lập trình đưa các bài toán thành 1 đối tượng cụ thể để giải quyết.

//  ĐỐI TƯỢNG LÀ GÌ ?
//  -   Đối tượng là một thực thể mà ta có thể thực hiện thao tác với

//  
//  Đối tượng là một thực thể trong thế giới thực mà chúng ta có thể nhìn thấy, cảm nhận và tương tác.
//  Ví dụ: Con mèo, con chó, cái bàn, cái ghế, cái tủ lạnh, cái máy tính, cái điện thoại, cái xe máy, cái ô tô

//  CÁC TÍNH CHẤT CỦA LẬP TRÌNH HƯỚNG ĐỐI TƯỢNG
//  - Tính đóng gói
//  - Tính kế thừa
//  - Tính đa hình
//  - Tính trừu tượng

//  TÍNH ĐÓNG GÓI
//  - Tính đóng gói sẽ được thể hiện thông qua Class(Lớp)
//  - Class sẽ đóng gối tất cả thuộc tính và phương thức
//  - Từ Class có thể tạo ra nhiều đối tượng có chung thuộc tính và phương thức trong Class
//  - Thuộc tính: là những gì giúp cấu thành đối tượng (Biến)
//  - Phương thức: là những hành động mà đối tượng có thể thực hiện (Hàm)

//  - Class là một khuôn mẫu (BluePrint) để tạo ra các đối tượng
//  - Thuộc tính (Properties) là các biến được định nghĩa trong Class,
//    đại diện cho các đặc điểm của đối tượng.

//  - Phương thức (Methods) là các hàm được định nghĩa trong Class,
//    đại diện cho các hành động mà đối tượng có thể thực hiện.


//  - Tính đóng gói (Encapsulation) là quá trình
//   nhóm các thuộc tính và phương thức của một đối tượng lại với nhau, 
//   ẩn đi các chi tiết bên trong và chỉ cung cấp các phương thức công khai để tương tác với đối tượng đó. 
//   Điều này giúp bảo vệ dữ liệu và giảm thiểu sự phụ thuộc giữa các đối tượng.